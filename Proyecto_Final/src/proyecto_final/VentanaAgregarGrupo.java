    private void txtNombreGrupoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreGrupoKeyPressed
        // TODO add your handling code here:
        if(nuevaInsercion.StringLengthValidator(this.txtNombreGrupo.getText(), 39) == false){
            String palabra = this.txtNombreGrupo.getText();
            this.txtNombreGrupo.setText(palabra.substring(0, 40));
        }
        
    }//GEN-LAST:event_txtNombreGrupoKeyPressed

    private void txtDescripcionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescripcionKeyPressed
        // TODO add your handling code here:
        if(nuevaInsercion.StringLengthValidator(this.txtDescripcion.getText(), 499) == false){
            String palabra = this.txtDescripcion.getText();
            this.txtDescripcion.setText(palabra.substring(0, 500));
        }
    }//GEN-LAST:event_txtDescripcionKeyPressed

